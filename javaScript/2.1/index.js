function countryInfo(country, population, capitalCity){
    return `${country} has ${population} people and its capital city is ${capitalCity}`;
}

const exampleOne= countryInfo("Israel", "8 million", "Jerusalem");
console.log (exampleOne);
const exampleTwo= countryInfo("USA", "330 million", "washington");
console.log (exampleTwo);
const exampleThree= countryInfo("England", "56 million", "London");
console.log (exampleThree);
